package com.company.PrimitiveDataTypes;

public class PrimitiveDatatypes {
    public static void main(String[] args) {
        int rollno = 64;
        float marks = 64.3f;
        double largeDecimalNumbers = 13164.1313;
        char letter = 'A';
        long largeInteger = 21312313;
        boolean check = false; // true || false

        System.out.println(rollno);
        System.out.println(marks);
        System.out.println(largeDecimalNumbers);
        System.out.println(letter);
        System.out.println(largeInteger);
        System.out.println(check);

    }
}
